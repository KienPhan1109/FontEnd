console.log(Math.abs(-5.5)); // Trả về 5.5
console.log(Math.abs(10)); // Trả về 10
// Hàm Math.abs sẽ trả về giá trị tuyệt đối

console.log(Math.ceil(4.4)) // Trả về 5
console.log(Math.ceil(5.8)) // Trả về 6
// Hàm Math.ceil sẽ trả về số nguyên lớn nhất

console.log(Math.floor(5.7)) // Trả về 5
console.log(Math.floor(6.1)) // Trả về 6
// Hàm Math.floor sẽ trả về số nguyên nhỏ nhất

console.log(Math.round(10.1)) // Trả về 10
console.log(Math.round(20.6)) // Trả về 21
// Hàm Math.round sẽ trả về số nguyên gần nhất